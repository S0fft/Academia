# Таблиця S1-боксу
S1 = [
    [14, 4, 13, 1, 2, 15, 11, 8, 3, 10, 6, 12, 5, 9, 0, 7],
    [0, 15, 7, 4, 14, 2, 13, 1, 10, 6, 12, 11, 9, 5, 3, 8],
    [4, 1, 14, 8, 13, 6, 2, 11, 15, 12, 9, 7, 3, 10, 5, 0],
    [15, 12, 8, 2, 4, 9, 1, 7, 5, 11, 3, 14, 10, 0, 6, 13]
]

# Вхідне значення b7
b7 = "111111"

# Отримуємо номер рядка (перший і останній біти)
row = int(b7[0] + b7[-1], 2)

# Отримуємо номер стовпчика (середні 4 біти)
column = int(b7[1:5], 2)

# Значення з S1-боксу
output = S1[row][column]

# Перетворення вихідного значення в бінарний формат
binary_output = f"{output:04b}"

print(f"Результат: {binary_output}")
